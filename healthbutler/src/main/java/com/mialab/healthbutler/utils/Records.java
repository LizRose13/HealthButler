package com.mialab.healthbutler.utils;

public class Records {
	
	private String from_to_times;
	private int run_icon;
	private int step_count;
	private int step_calori;
	private int time;
	public int getRun_icon() {
		return run_icon;
	}
	public void setRun_icon(int run_icon) {
		this.run_icon = run_icon;
	}
	public int getStep_count() {
		return step_count;
	}
	public void setStep_count(int step_count) {
		this.step_count = step_count;
	}
	public int getStep_calori() {
		return step_calori;
	}
	public void setStep_calori(int step_calori) {
		this.step_calori = step_calori;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	public String getFrom_to_times() {
		return from_to_times;
	}
	public void setFrom_to_times(String from_to_times) {
		this.from_to_times = from_to_times;
	}

}
